package bean;

public class Cart {

	   private String c_id;
	   private String c_p_code;
	   private int c_qty;
	   
	   
	   public String getC_id() {
	      return c_id;
	   }
	   public void setC_id(String c_id) {
	      this.c_id = c_id;
	   }
	   public String getC_p_code() {
	      return c_p_code;
	   }
	   public void setC_p_code(String c_p_code) {
	      this.c_p_code = c_p_code;
	   }
	   public int getC_qty() {
	      return c_qty;
	   }
	   public void setC_qty(int c_qty) {
	      this.c_qty = c_qty;
	   }

}
